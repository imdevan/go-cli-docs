type FlagInfo struct {
	Name        string `json:"name"`
	Short       string `json:"short"`
	Type        string `json:"type"`
	Description string `json:"description"`
	Persistent  bool   `json:"persistent"`
}

type FlagGroupInfo struct {
	Name        string     `json:"name"`
	Description string     `json:"description"`
	Example     string     `json:"example"`
	Note        string     `json:"note"`
	Flags       []FlagInfo `json:"flags"`
}

type CommandInfo struct {
	GoFile     string          `json:"go_file"`
	CmdName    string          `json:"cmd_name"` // from godoc comment first word
	VarName    string          `json:"var_name"`
	Doc        string          `json:"doc"`
	Use        string          `json:"use"`
	Short      string          `json:"short"`
	HasCommand bool            `json:"has_command"`
	FlagGroups []FlagGroupInfo `json:"flag_groups"`
	Path       []string        `json:"path"`
}

func parseCommands(dir string) ([]CommandInfo, error) {
	pkgName := filepath.Base(dir)

	// 1. Find root command symbol in the Go source files
	symbolName, isFunc, err := findRootCmdSymbol(dir)
	if err != nil {
		return nil, fmt.Errorf("failed to find root command symbol: %w", err)
	}

	rootExpr := symbolName
	if isFunc {
		rootExpr = symbolName + "()"
	}

	// 2. Write dynamic test file docs_exporter_tmp_test.go
	testPath := filepath.Join(dir, "docs_exporter_tmp_test.go")
	jsonPath := filepath.Join(dir, "docs_metadata_tmp.json")
	defer func() {
		_ = os.Remove(testPath)
		_ = os.Remove(jsonPath)
	}()

	testContent := fmt.Sprintf(strings.ReplaceAll(exporterTestTemplate, "#TAG#", "\x60"), rootExpr)
	if err := os.WriteFile(testPath, []byte(testContent), 0644); err != nil {
		return nil, fmt.Errorf("failed to write dynamic test file: %w", err)
	}

	// 3. Run go test to execute the exporter
	cmd := exec.Command("go", "test", "-run", "^TestExportDocs$", "./"+dir)
	output, err := cmd.CombinedOutput()
	if err != nil {
		return nil, fmt.Errorf("failed to execute dynamic exporter via go test: %w\nOutput:\n%s", err, string(output))
	}

	// 4. Read the generated metadata JSON file
	jsonData, err := os.ReadFile(jsonPath)
	if err != nil {
		return nil, fmt.Errorf("failed to read metadata generated by exporter: %w", err)
	}

	var exportedRoot ExportedCommand
	if err := json.Unmarshal(jsonData, &exportedRoot); err != nil {
		return nil, fmt.Errorf("failed to parse metadata generated by exporter: %w", err)
	}

	fmt.Printf("DEBUG ROOT: %s, SUBCOMMANDS: ", exportedRoot.Name)
	for _, sub := range exportedRoot.Subcommands {
		fmt.Printf("%s, ", sub.Name)
	}
	fmt.Println()

	// 5. Parse source comments from all Go files in dir
	cmdComments, err := parseCommentsFromDir(dir)
	if err != nil {
		return nil, fmt.Errorf("failed to parse comments from source: %w", err)
	}

	if Debug {
		for _, fc := range cmdComments {
			fmt.Printf("DEBUG FILE COMMENT: File: %s, CmdName: %s, NumGroups: %d\n", fc.GoFile, fc.CmdName, len(fc.FlagGroups))
		}
	}

	// 6. Flatten the command tree and merge with comments/flag groups
	flatCmds := flattenCommands(exportedRoot, nil, pkgName, cmdComments)
	
	fmt.Printf("DEBUG FLAT CMDS: ")
	for _, c := range flatCmds {
		fmt.Printf("%s, ", c.CmdName)
	}
	fmt.Println()

	if Debug {
		for _, c := range flatCmds {
			fmt.Printf("DEBUG FLAT CMD: %s (Path: %v, GoFile: %s)\n", c.CmdName, c.Path, c.GoFile)
		}
	}

	return flatCmds, nil
}

