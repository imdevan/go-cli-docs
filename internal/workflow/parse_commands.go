func exportCmd(cmd *cobra.Command) ExportedCommand {
	ec := ExportedCommand{
		Name:  cmd.Name(),
		Use:   cmd.Use,
		Short: cmd.Short,
		Long:  cmd.Long,
	}

	cmd.LocalFlags().VisitAll(func(f *pflag.Flag) {
		ec.Flags = append(ec.Flags, ExportedFlag{
			Name:        f.Name,
			Short:       f.Shorthand,
			Type:        f.Value.Type(),
			Description: f.Usage,
			Persistent:  false,
			DefValue:    f.DefValue,
		})
	})

	cmd.PersistentFlags().VisitAll(func(f *pflag.Flag) {
		for _, lf := range ec.Flags {
			if lf.Name == f.Name {
				return
			}
		}
		ec.Flags = append(ec.Flags, ExportedFlag{
			Name:        f.Name,
			Short:       f.Shorthand,
			Type:        f.Value.Type(),
			Description: f.Usage,
			Persistent:  true,
			DefValue:    f.DefValue,
		})
	})

	for _, sub := range cmd.Commands() {
		if sub.Name() == "help" {
			continue
		}
		ec.Subcommands = append(ec.Subcommands, exportCmd(sub))
	}

	return ec
}

