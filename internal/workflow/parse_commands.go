type parsedFileComments struct {
	GoFile     string
	CmdName    string
	DocBlock   *parsedCommandBlock
	FlagGroups []parsedFlagGroupBlock
}

type parsedCommandBlock struct {
	Name        string
	Description string
	Example     string
	Note        string
}

func findRootCmdSymbol(dir string) (string, bool, error) {
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, dir, func(fi os.FileInfo) bool {
		return !strings.HasSuffix(fi.Name(), "_test.go")
	}, parser.ParseComments)
	if err != nil {
		return "", false, err
	}

	isCobraCmdType := func(expr ast.Expr) bool {
		if expr == nil {
			return false
		}
		star, ok := expr.(*ast.StarExpr)
		if !ok {
			return false
		}
		sel, ok := star.X.(*ast.SelectorExpr)
		if !ok {
			return false
		}
		xIdent, ok := sel.X.(*ast.Ident)
		if !ok {
			return false
		}
		return xIdent.Name == "cobra" && sel.Sel.Name == "Command"
	}

	var fallbackVar string
	var fallbackFunc string

	for _, pkg := range pkgs {
		for _, file := range pkg.Files {
			for _, decl := range file.Decls {
				switch d := decl.(type) {
				case *ast.GenDecl:
					if d.Tok == token.VAR {
						for _, spec := range d.Specs {
							vspec, ok := spec.(*ast.ValueSpec)
							if !ok {
								continue
							}
							isCmd := false
							if vspec.Type != nil && isCobraCmdType(vspec.Type) {
								isCmd = true
							} else {
								for _, val := range vspec.Values {
									if unary, ok := val.(*ast.UnaryExpr); ok && unary.Op == token.AND {
										if comp, ok := unary.X.(*ast.CompositeLit); ok {
											if sel, ok := comp.Type.(*ast.SelectorExpr); ok {
												if xIdent, ok := sel.X.(*ast.Ident); ok && xIdent.Name == "cobra" && sel.Sel.Name == "Command" {
													isCmd = true
												}
											}
										}
									}
								}
							}
							if isCmd && len(vspec.Names) > 0 {
								name := vspec.Names[0].Name
								if strings.Contains(strings.ToLower(name), "root") {
									return name, false, nil
								}
								fallbackVar = name
							}
						}
					}
				case *ast.FuncDecl:
					isCmd := false
					if d.Type.Results != nil {
						for _, field := range d.Type.Results.List {
							if isCobraCmdType(field.Type) {
								isCmd = true
								break
							}
						}
					}
					if isCmd {
						name := d.Name.Name
						if strings.Contains(strings.ToLower(name), "root") {
							return name, true, nil
						}
						fallbackFunc = name
					}
				}
			}
		}
	}

	if fallbackVar != "" {
		return fallbackVar, false, nil
	}
	if fallbackFunc != "" {
		return fallbackFunc, true, nil
	}

	return "", false, fmt.Errorf("could not find a cobra.Command variable or function in package")
}

func parseCommentsFromDir(dir string) ([]parsedFileComments, error) {
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, dir, func(fi os.FileInfo) bool {
		return !strings.HasSuffix(fi.Name(), "_test.go")
	}, parser.ParseComments)
	if err != nil {
		return nil, err
	}

	var results []parsedFileComments

	for _, pkg := range pkgs {
		for fname, file := range pkg.Files {
			var fc parsedFileComments
			fc.GoFile = filepath.Join(dir, filepath.Base(fname))

			for _, cg := range file.Comments {
				text := cg.Text()
				if name, desc, eg, note, ok := parseDocsCommandBlock(text); ok {
					fc.CmdName = name
					fc.DocBlock = &parsedCommandBlock{
						Name:        name,
						Description: desc,
						Example:     eg,
						Note:        note,
					}
				}
				if fgBlock, ok := parseDocsFlagGroupBlock(text); ok {
					fc.FlagGroups = append(fc.FlagGroups, fgBlock)
				}
			}

			if fc.CmdName == "" {
				var foundUse string
				ast.Inspect(file, func(n ast.Node) bool {
					unary, ok := n.(*ast.UnaryExpr)
					if !ok || unary.Op != token.AND {
						return true
					}
					comp, ok := unary.X.(*ast.CompositeLit)
					if !ok {
						return true
					}
					sel, ok := comp.Type.(*ast.SelectorExpr)
					if !ok {
						return true
					}
					xIdent, ok := sel.X.(*ast.Ident)
					if !ok || xIdent.Name != "cobra" || sel.Sel.Name != "Command" {
						return true
					}

					for _, el := range comp.Elts {
						kv, ok := el.(*ast.KeyValueExpr)
						if !ok {
							continue
						}
						keyIdent, ok := kv.Key.(*ast.Ident)
						if !ok {
							continue
						}
						if keyIdent.Name == "Use" {
							foundUse = parseExprString(kv.Value, file)
						}
					}
					return false
				})

				if foundUse != "" {
					words := strings.Fields(foundUse)
					if len(words) > 0 {
						fc.CmdName = words[0]
					}
				}
			}

			if fc.CmdName == "" && filepath.Base(fname) == "root.go" {
				fc.CmdName = "root"
			}

			results = append(results, fc)
		}
	}

	return results, nil
}

func flattenCommands(ec ExportedCommand, parentPath []string, pkgName string, fileComments []parsedFileComments) []CommandInfo {
	var currentPath []string
	if parentPath == nil {
		currentPath = []string{pkgName}
	} else {
		currentPath = append(append([]string{}, parentPath...), ec.Name)
	}

	cmdName := strings.Join(currentPath, "-")

	info := CommandInfo{
		CmdName:    cmdName,
		Use:        ec.Use,
		Short:      ec.Short,
		HasCommand: true,
		Path:       currentPath,
	}

	var matchingComment *parsedFileComments
	for i := range fileComments {
		fc := &fileComments[i]
		if len(parentPath) == 0 {
			if fc.CmdName == "root" || fc.CmdName == pkgName || strings.HasSuffix(fc.GoFile, "root.go") {
				matchingComment = fc
				break
			}
		} else {
			if fc.CmdName == ec.Name {
				matchingComment = fc
				break
			}
		}
	}

	if matchingComment != nil {
		info.GoFile = matchingComment.GoFile
		if matchingComment.DocBlock != nil {
			var docParts []string
			if matchingComment.DocBlock.Description != "" {
				docParts = append(docParts, matchingComment.DocBlock.Description)
			}
			if matchingComment.DocBlock.Example != "" {
				docParts = append(docParts, "### Example\n\n"+matchingComment.DocBlock.Example)
			}
			if matchingComment.DocBlock.Note != "" {
				docParts = append(docParts, ":::note\n"+matchingComment.DocBlock.Note+"\n:::")
			}
			info.Doc = strings.Join(docParts, "\n\n")
		}
	}

	if info.Doc == "" {
		if ec.Long != "" {
			info.Doc = ec.Long
		} else {
			info.Doc = ec.Short
		}
	}

	var allFlags []FlagInfo
	for _, f := range ec.Flags {
		allFlags = append(allFlags, FlagInfo{
			Name:        f.Name,
			Short:       f.Short,
			Type:        f.Type,
			Description: f.Description,
			Persistent:  f.Persistent,
		})
	}

	assigned := make(map[string]bool)
	var flagGroups []FlagGroupInfo

	if matchingComment != nil && len(matchingComment.FlagGroups) > 0 {
		for _, b := range matchingComment.FlagGroups {
			group := FlagGroupInfo{
				Name:        b.Name,
				Description: b.Description,
				Example:     b.Example,
				Note:        b.Note,
			}
			for _, fName := range b.FlagNames {
				if Debug {
					fmt.Printf("DEBUG MATCHING fName: %q for group: %s\n", fName, b.Name)
				}
				for _, f := range allFlags {
					if Debug {
						fmt.Printf("  COMPARING with f.Name: %q\n", f.Name)
					}
					if f.Name == fName {
						group.Flags = append(group.Flags, f)
						assigned[f.Name] = true
						break
					}
				}
			}
			flagGroups = append(flagGroups, group)
		}
	}

	var unassignedFlags []FlagInfo
	for _, f := range allFlags {
		if !assigned[f.Name] {
			unassignedFlags = append(unassignedFlags, f)
		}
	}

	if len(unassignedFlags) > 0 {
		flagGroups = append(flagGroups, FlagGroupInfo{
			Name:  "Flags",
			Flags: unassignedFlags,
		})
	}

	info.FlagGroups = flagGroups

	var subParentPath []string
	if parentPath == nil {
		subParentPath = []string{}
	} else {
		subParentPath = currentPath
	}

	result := []CommandInfo{info}
	for _, sub := range ec.Subcommands {
		result = append(result, flattenCommands(sub, subParentPath, pkgName, fileComments)...)
	}

	return result
}

