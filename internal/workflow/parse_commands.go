func parseExprString(expr ast.Expr, file *ast.File) string {
	switch e := expr.(type) {
	case *ast.BasicLit:
		if e.Kind == token.STRING {
			return strings.Trim(e.Value, "`\"")
		}
	case *ast.Ident:
		if file != nil {
			if val := lookupIdentValue(e.Name, file); val != "" {
				return val
			}
		}
		return e.Name
	case *ast.BinaryExpr:
		if e.Op == token.ADD {
			return parseExprString(e.X, file) + parseExprString(e.Y, file)
		}
	}
	return ""
}

func lookupIdentValue(name string, file *ast.File) string {
	if file == nil {
		return ""
	}
	for _, decl := range file.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || (genDecl.Tok != token.CONST && genDecl.Tok != token.VAR) {
			continue
		}
		for _, spec := range genDecl.Specs {
			vspec, ok := spec.(*ast.ValueSpec)
			if !ok {
				continue
			}
			for i, ident := range vspec.Names {
				if ident.Name == name {
					if i < len(vspec.Values) {
						return parseExprString(vspec.Values[i], nil)
					}
				}
			}
		}
	}
	return ""
}

func parseCommentGroup(text string) (groupName, desc, example string, isGroup bool) {
	lines := strings.Split(text, "\n")
	var parsingDesc, parsingExample bool
	var descLines, exampleLines []string

	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "@group:") {
			groupName = strings.TrimSpace(strings.TrimPrefix(trimmed, "@group:"))
			isGroup = true
			parsingDesc = false
			parsingExample = false
			continue
		}
		if strings.HasPrefix(trimmed, "@description:") {
			parsingDesc = true
			parsingExample = false
			rest := strings.TrimSpace(strings.TrimPrefix(trimmed, "@description:"))
			if rest != "" {
				descLines = append(descLines, rest)
			}
			continue
		}
		if strings.HasPrefix(trimmed, "@example:") {
			parsingExample = true
			parsingDesc = false
			rest := strings.TrimSpace(strings.TrimPrefix(trimmed, "@example:"))
			if rest != "" {
				exampleLines = append(exampleLines, rest)
			}
			continue
		}

		if parsingDesc {
			descLines = append(descLines, line)
		} else if parsingExample {
			exampleLines = append(exampleLines, line)
		}
	}

	if isGroup {
		desc = cleanBlockText(descLines)
		example = cleanBlockText(exampleLines)
	}
	return
}

func cleanBlockText(lines []string) string {
	if len(lines) == 0 {
		return ""
	}
	minChars := -1
	for _, l := range lines {
		if strings.TrimSpace(l) == "" {
			continue
		}
		chars := 0
		for _, r := range l {
			if r == ' ' || r == '\t' {
				chars++
			} else {
				break
			}
		}
		if minChars == -1 || chars < minChars {
			minChars = chars
		}
	}
	if minChars == -1 {
		minChars = 0
	}

	var cleaned []string
	for _, l := range lines {
		if len(l) <= minChars {
			cleaned = append(cleaned, "")
		} else {
			cleaned = append(cleaned, strings.TrimRight(l[minChars:], " \t"))
		}
	}
	start := 0
	for start < len(cleaned) && cleaned[start] == "" {
		start++
	}
	end := len(cleaned)
	for end > start && cleaned[end-1] == "" {
		end--
	}
	return strings.Join(cleaned[start:end], "\n")
}

func parseGoFile(filePath string) (CommandInfo, error) {
	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, filePath, nil, parser.ParseComments)
	if err != nil {
		return CommandInfo{}, err
	}

	info := CommandInfo{
		GoFile: filePath,
	}

	// Helper to check if a type is *cobra.Command
	isCobraCmdType := func(expr ast.Expr) bool {
		star, ok := expr.(*ast.StarExpr)
		if !ok {
			return false
		}
		sel, ok := star.X.(*ast.SelectorExpr)
		if !ok {
			return false
		}
		xIdent, ok := sel.X.(*ast.Ident)
		if !ok {
			return false
		}
		return xIdent.Name == "cobra" && sel.Sel.Name == "Command"
	}

	// Helper to extract flag info from a CallExpr
	extractFlagInfo := func(call *ast.CallExpr) (name, short, flagType, description string, persistent, ok bool) {
		sel, okSel := call.Fun.(*ast.SelectorExpr)
		if !okSel {
			return "", "", "", "", false, false
		}
		funName := sel.Sel.Name
		recvCall, okRecvCall := sel.X.(*ast.CallExpr)
		if !okRecvCall {
			return "", "", "", "", false, false
		}
		recvSel, okRecvSel := recvCall.Fun.(*ast.SelectorExpr)
		if !okRecvSel || (recvSel.Sel.Name != "Flags" && recvSel.Sel.Name != "PersistentFlags") {
			return "", "", "", "", false, false
		}
		persistent = recvSel.Sel.Name == "PersistentFlags"

		isFlagMethod := false
		for _, prefix := range []string{"Bool", "String", "Int", "Duration", "Float", "Var"} {
			if strings.HasPrefix(funName, prefix) {
				isFlagMethod = true
				break
			}
		}
		if !isFlagMethod {
			return "", "", "", "", false, false
		}

		args := call.Args
		if strings.HasSuffix(funName, "VarP") {
			if len(args) >= 5 {
				name = parseExprString(args[1], file)
				short = parseExprString(args[2], file)
				description = parseExprString(args[4], file)
				flagType = strings.TrimSuffix(funName, "VarP")
				ok = true
			}
		} else if strings.HasSuffix(funName, "Var") {
			if len(args) >= 4 {
				name = parseExprString(args[1], file)
				short = ""
				description = parseExprString(args[3], file)
				flagType = strings.TrimSuffix(funName, "Var")
				ok = true
			}
		} else {
			if strings.HasSuffix(funName, "P") {
				if len(args) >= 4 {
					name = parseExprString(args[0], file)
					short = parseExprString(args[1], file)
					description = parseExprString(args[3], file)
					flagType = strings.TrimSuffix(funName, "P")
					ok = true
				}
			} else {
				if len(args) >= 3 {
					name = parseExprString(args[0], file)
					short = ""
					description = parseExprString(args[2], file)
					flagType = funName
					ok = true
				}
			}
		}
		return
	}

	// Helper to find &cobra.Command inside a node (e.g. FuncDecl or ValueSpec)
	findCobraCommandInNode := func(node ast.Node) (use, short string, found bool) {
		ast.Inspect(node, func(n ast.Node) bool {
			unary, ok := n.(*ast.UnaryExpr)
			if !ok || unary.Op != token.AND {
				return true
			}
			comp, ok := unary.X.(*ast.CompositeLit)
			if !ok {
				return true
			}
			sel, ok := comp.Type.(*ast.SelectorExpr)
			if !ok {
				return true
			}
			xIdent, ok := sel.X.(*ast.Ident)
			if !ok || xIdent.Name != "cobra" || sel.Sel.Name != "Command" {
				return true
			}

			found = true
			for _, el := range comp.Elts {
				kv, ok := el.(*ast.KeyValueExpr)
				if !ok {
					continue
				}
				keyIdent, ok := kv.Key.(*ast.Ident)
				if !ok {
					continue
				}
				if keyIdent.Name == "Use" {
					use = parseExprString(kv.Value, file)
				} else if keyIdent.Name == "Short" {
					short = parseExprString(kv.Value, file)
				}
			}
			return false
		})
		return
	}

	// Inspect top-level declarations to find command definition and set basic info
	for _, decl := range file.Decls {
		switch d := decl.(type) {
		case *ast.GenDecl:
			if d.Tok == token.VAR {
				for _, spec := range d.Specs {
					vspec, ok := spec.(*ast.ValueSpec)
					if !ok {
						continue
					}
					isCmd := false
					if vspec.Type != nil && isCobraCmdType(vspec.Type) {
						isCmd = true
					}
					var use, short string
					for _, val := range vspec.Values {
						if u, s, ok := findCobraCommandInNode(val); ok {
							isCmd = true
							if use == "" {
								use = u
							}
							if short == "" {
								short = s
							}
						}
					}
					if isCmd {
						info.HasCommand = true
						if len(vspec.Names) > 0 {
							info.VarName = vspec.Names[0].Name
						}
						info.Use = use
						info.Short = short
						var commentGroup *ast.CommentGroup
						if vspec.Doc != nil {
							commentGroup = vspec.Doc
						} else if d.Doc != nil {
							commentGroup = d.Doc
						}
						setDocAndName(&info, commentGroup)
						break
					}
				}
			}
		case *ast.FuncDecl:
			isCmd := false
			if d.Type.Results != nil {
				for _, field := range d.Type.Results.List {
					if isCobraCmdType(field.Type) {
						isCmd = true
						break
					}
				}
			}

			if isCmd {
				info.HasCommand = true
				info.VarName = d.Name.Name
				if u, s, ok := findCobraCommandInNode(d.Body); ok {
					info.Use = u
					info.Short = s
				}
				setDocAndName(&info, d.Doc)
				break
			}
		}
		if info.HasCommand {
			break
		}
	}

	if info.HasCommand {
		type ASTNodeWithLine struct {
			Line     int
			Comment  *ast.CommentGroup
			FlagCall *ast.CallExpr
		}
		var items []ASTNodeWithLine

		for _, cg := range file.Comments {
			items = append(items, ASTNodeWithLine{
				Line:    fset.Position(cg.Pos()).Line,
				Comment: cg,
			})
		}

		ast.Inspect(file, func(n ast.Node) bool {
			if call, ok := n.(*ast.CallExpr); ok {
				if _, _, _, _, _, isFlag := extractFlagInfo(call); isFlag {
					items = append(items, ASTNodeWithLine{
						Line:     fset.Position(call.Pos()).Line,
						FlagCall: call,
					})
				}
			}
			return true
		})

		sort.Slice(items, func(i, j int) bool {
			return items[i].Line < items[j].Line
		})

		// Check if we have any @docs-flag-group block in the file comments
		hasDocsFlagGroup := false
		for _, item := range items {
			if item.Comment != nil {
				txt := item.Comment.Text()
				if strings.Contains(txt, "@docs-flag-group:") || strings.Contains(txt, "@docs-flag-group") {
					hasDocsFlagGroup = true
					break
				}
			}
		}

		if hasDocsFlagGroup {
			// New System: organize flags based on @docs-flag-group blocks
			var parsedBlocks []parsedFlagGroupBlock
			for _, item := range items {
				if item.Comment != nil {
					block, parsed := parseDocsFlagGroupBlock(item.Comment.Text())
					if parsed {
						parsedBlocks = append(parsedBlocks, block)
					}
				}
			}

			// Gather all flags parsed from the AST
			var allFlags []FlagInfo
			for _, item := range items {
				if item.FlagCall != nil {
					name, short, flagType, desc, persistent, ok := extractFlagInfo(item.FlagCall)
					if ok {
						allFlags = append(allFlags, FlagInfo{
							Name:        name,
							Short:       short,
							Type:        flagType,
							Description: desc,
							Persistent:  persistent,
						})
					}
				}
			}

			// Track assigned flags
			assigned := make(map[string]bool)
			var flagGroups []FlagGroupInfo

			for _, b := range parsedBlocks {
				group := FlagGroupInfo{
					Name:        b.Name,
					Description: b.Description,
					Example:     b.Example,
					Note:        b.Note,
				}
				for _, fName := range b.FlagNames {
					// Find the flag in parsed flags
					for _, f := range allFlags {
						if f.Name == fName {
							group.Flags = append(group.Flags, f)
							assigned[f.Name] = true
							break
						}
					}
				}
				flagGroups = append(flagGroups, group)
			}

			// Add unassigned flags to default group
			var unassignedFlags []FlagInfo
			for _, f := range allFlags {
				if !assigned[f.Name] {
					unassignedFlags = append(unassignedFlags, f)
				}
			}

			if len(unassignedFlags) > 0 {
				flagGroups = append(flagGroups, FlagGroupInfo{
					Name:  "Flags",
					Flags: unassignedFlags,
				})
			}

			info.FlagGroups = flagGroups

		} else {
			// Old System: fallback to @group: comments and encounter order
			var flagGroups []FlagGroupInfo
			var currentGroup *FlagGroupInfo

			for _, item := range items {
				if item.Comment != nil {
					gName, desc, example, isGroup := parseCommentGroup(item.Comment.Text())
					if isGroup {
						if currentGroup != nil {
							flagGroups = append(flagGroups, *currentGroup)
						}
						currentGroup = &FlagGroupInfo{
							Name:        gName,
							Description: desc,
							Example:     example,
						}
					}
				} else if item.FlagCall != nil {
					name, short, flagType, desc, persistent, ok := extractFlagInfo(item.FlagCall)
					if ok {
						if currentGroup == nil {
							currentGroup = &FlagGroupInfo{
								Name: "Flags",
							}
						}
						currentGroup.Flags = append(currentGroup.Flags, FlagInfo{
							Name:        name,
							Short:       short,
							Type:        flagType,
							Description: desc,
							Persistent:  persistent,
						})
					}
				}
			}
			if currentGroup != nil {
				flagGroups = append(flagGroups, *currentGroup)
			}

			info.FlagGroups = flagGroups
		}
	}

	return info, nil
}

func setDocAndName(info *CommandInfo, commentGroup *ast.CommentGroup) {
	if commentGroup == nil {
		return
	}
	docText := commentGroup.Text()
	name, description, example, note, parsed := parseDocsCommandBlock(docText)
	if parsed {
		if name != "" {
			info.CmdName = name
		}
		var docParts []string
		if description != "" {
			docParts = append(docParts, description)
		}
		if example != "" {
			docParts = append(docParts, "### Example\n\n"+example)
		}
		if note != "" {
			docParts = append(docParts, ":::note\n"+note+"\n:::")
		}
		info.Doc = strings.Join(docParts, "\n\n")
	} else {
		info.Doc = strings.TrimSpace(docText)
		lines := strings.Split(info.Doc, "\n")
		if len(lines) > 0 && len(lines[0]) > 0 {
			words := strings.Fields(lines[0])
			if len(words) > 0 {
				info.CmdName = strings.Trim(words[0], "`\"'")
			}
		}
	}
}

type parsedFlagGroupBlock struct {
	Name        string
	Description string
	Example     string
	Note        string
	FlagNames   []string
}

func parseDocsFlagGroupBlock(text string) (block parsedFlagGroupBlock, parsed bool) {
	lines := strings.Split(text, "\n")
	startIndex := -1
	for i, line := range lines {
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "@docs-flag-group:") || strings.HasPrefix(trimmed, "@docs-flag-group") {
			startIndex = i
			break
		}
	}
	if startIndex == -1 {
		return
	}
	parsed = true

	var currentKey string
	var currentValLines []string

	flush := func() {
		if currentKey == "" {
			return
		}
		val := cleanBlockText(currentValLines)
		switch currentKey {
		case "name":
			block.Name = val
		case "description":
			block.Description = val
		case "example":
			block.Example = val
		case "note":
			block.Note = val
		case "flags":
			for _, part := range strings.Split(val, ",") {
				trimmedPart := strings.TrimSpace(part)
				if trimmedPart != "" {
					block.FlagNames = append(block.FlagNames, trimmedPart)
				}
			}
		}
		currentKey = ""
		currentValLines = nil
	}

	for _, line := range lines[startIndex+1:] {
		trimmed := strings.TrimSpace(line)
		var nextKey string
		for _, k := range []string{"name:", "description:", "example:", "note:", "flags:"} {
			if strings.HasPrefix(trimmed, k) {
				nextKey = strings.TrimSuffix(k, ":")
				break
			}
		}

		if nextKey != "" {
			flush()
			currentKey = nextKey
			rest := strings.TrimSpace(strings.TrimPrefix(trimmed, currentKey+":"))
			if rest != "" {
				currentValLines = append(currentValLines, rest)
			}
		} else {
			if currentKey != "" {
				currentValLines = append(currentValLines, line)
			}
		}
	}
	flush()
	return
}

